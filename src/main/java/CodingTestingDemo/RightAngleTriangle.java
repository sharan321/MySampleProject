package CodingTestingDemo;

public class RightAngleTriangle
{
    public static void main(String[] args) {
        System.out.println("Printing right angle triangle");
        for(int i=0;i<5;i++)
        {
            for(int j=0;j<=i;j++)
            {
                System.out.print("*");
            }
            System.out.println();
        }
       System.out.println("");

    }
}
